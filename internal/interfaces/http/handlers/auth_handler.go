// PasskeyPresence reports whether the user already enrolled a passkey.
type PasskeyPresence interface {
	HasPasskey(ctx context.Context, tenantID, email string) bool
}

// AuthHandler is the HTTP surface for OTP / consent / refresh / logout.
type AuthHandler struct {
	otp      *identity.OTPService
	tokens   *identity.TokenService
	users    identity.UserStore
	tenants  mw.TenantResolver // for email domain → tenant resolution
	audit    audit.Store
	rl       identity.RateLimiter
	passkeys PasskeyPresence // optional
	pepper   string
}

// NewAuthHandler constructs an AuthHandler with required dependencies.
func NewAuthHandler(
	otp *identity.OTPService,
	tokens *identity.TokenService,
	users identity.UserStore,
	tenants mw.TenantResolver,
	auditStore audit.Store,
	rl identity.RateLimiter,
	passkeys PasskeyPresence,
	pepper string,
) *AuthHandler {
	return &AuthHandler{
		otp:      otp,
		tokens:   tokens,
		users:    users,
		tenants:  tenants,
		audit:    auditStore,
		rl:       rl,
		passkeys: passkeys,
		pepper:   pepper,
	}
}

// Mount registers auth public routes.
func (h *AuthHandler) Mount(r chi.Router) {
	r.Get("/", h.LoginPage)
	r.Post("/auth/otp/send", h.SendOTP)
	r.Post("/auth/otp/verify", h.VerifyOTP)
	r.Get("/auth/consent", h.ConsentPage)
	r.Post("/auth/consent", h.ConsentAccept)
	r.Post("/auth/refresh", h.Refresh)
	r.Post("/auth/logout", h.Logout)
}

func (h *AuthHandler) LoginPage(w http.ResponseWriter, r *http.Request) {
	if httputil.WantsJSON(r) {
		httputil.WriteJSON(w, http.StatusOK, map[string]string{"type": "login", "message": "Login form endpoint"})
		return
	}
	templ.Handler(authtmpl.LoginShell()).ServeHTTP(w, r)
}

func (h *AuthHandler) SendOTP(w http.ResponseWriter, r *http.Request) {
	if err := r.ParseForm(); err != nil {
		httputil.WriteJSON(w, http.StatusBadRequest, map[string]string{"error": "bad request"})
		return
	}
	email := strings.TrimSpace(r.FormValue("email"))
	if email == "" || !strings.Contains(email, "@") {
		templ.Handler(authtmpl.LoginFormPartial()).ServeHTTP(w, r)
		return
	}
	mw.SetLogEmail(r, email)

	if h.rl != nil {
		allowed, err := h.rl.Allow(r.Context(), identity.RateKeyOTPSend(email), identity.PolicyOTP)
		if err != nil {
			slog.Warn("rate limit error", "err", err)
		}
		if !allowed {
			msg := "Too many attempts. Please wait before trying again."
			templ.Handler(authtmpl.OTPErrorPartial(email, msg)).ServeHTTP(w, r)
			return
		}
	}

	if _, err := h.otp.Send(r.Context(), email); err != nil {
		slog.Error("OTP send failed", "err", err)
		templ.Handler(authtmpl.LoginFormPartial()).ServeHTTP(w, r)
		return
	}

	// Resolve tenant from EMAIL domain (not from cookie/router)
	tenantID := "public"
	if domain := emailDomain(email); domain != "" && h.tenants != nil {
		if code, err := h.tenants.ResolveByDomain(r.Context(), domain); err == nil && code != "" {
			tenantID = code
		}
	}

	// Find or create user
	emailHash := security.HashEmail(email, h.pepper)
	u, err := h.users.FindByEmailHash(r.Context(), emailHash)
	userID := ""
	if err == nil && u != nil {
		userID = u.UserID
	}
	if userID == "" {
		// Create new user with UUID v7
		newUser := &identity.User{
			UserID:    "", // will be generated by repo
			EmailHash: emailHash,
			TenantID:  tenantID,
			Status:    "pending",
			IsActive:  false,
			CreatedAt: time.Now(),
			UpdatedAt: time.Now(),
		}
		if err := h.users.Create(r.Context(), newUser); err != nil {
			slog.Error("cannot create user", "email", security.MaskEmail(email), "err", err)
			http.Error(w, "internal error", http.StatusInternalServerError)
			return
		}
		userID = newUser.UserID
	}

	// No pre-auth cookie — tenant resolved from email domain on every request

	h.logSecurity(r, audit.SecurityEvent{
		UserID:      userID,
		TenantID:    tenantID,
		MaskedEmail: security.MaskEmail(email),
		Action:      "auth.otp.send",
		Status:      "success",
		ClientIP:    mw.GetClientIP(r),
		ReqID:       mw.GetRequestID(r.Context()),
	})

	templ.Handler(authtmpl.OTPFormPartial(email)).ServeHTTP(w, r)
}

func (h *AuthHandler) VerifyOTP(w http.ResponseWriter, r *http.Request) {
	if err := r.ParseForm(); err != nil {
		http.Error(w, "bad request", http.StatusBadRequest)
		return
	}
	email := r.FormValue("email")
	mw.SetLogEmail(r, email)
	codes := r.Form["code"]
	code := strings.Join(codes, "")

	if h.rl != nil {
		allowed, err := h.rl.Allow(r.Context(), identity.RateKeyOTPFail(email), identity.PolicyOTP)
		if err != nil {
			slog.Warn("rate limit error", "err", err)
		}
		if !allowed {
			msg := "Too many attempts. Please wait before trying again."
			templ.Handler(authtmpl.OTPErrorPartial(email, msg)).ServeHTTP(w, r)
			return
		}
	}

	if !h.otp.Verify(email, code) {
		h.logSecurity(r, audit.SecurityEvent{
			MaskedEmail: security.MaskEmail(email),
			Action:      "auth.otp.verify",
			Status:      "failure",
			ClientIP:    mw.GetClientIP(r),
			ReqID:       mw.GetRequestID(r.Context()),
		})
		templ.Handler(authtmpl.OTPErrorPartial(email, "Invalid or expired code. Try again.")).ServeHTTP(w, r)
		return
	}

	if h.rl != nil {
		_ = h.rl.Reset(r.Context(), identity.RateKeyOTPSend(email))
		_ = h.rl.Reset(r.Context(), identity.RateKeyOTPFail(email))
	}

	emailHash := security.HashEmail(email, h.pepper)
	u, err := h.users.FindByEmailHash(r.Context(), emailHash)
	if err != nil || u == nil || u.UserID == "" {
		slog.Error("user not found after OTP verify", "email", security.MaskEmail(email))
		http.Error(w, "user not found", http.StatusBadRequest)
		return
	}

	tenantID := u.TenantID
	if tenantID == "" {
		tenantID = "public"
	}

	// MFA check: if tenant requires MFA and user has no passkey → require enrollment
	if h.passkeys != nil {
		// TODO: read tenant.SecurityPolicy.MFARequired from TenantStore
		// For now, check if user has passkey
		hasPasskey := h.passkeys.HasPasskey(r.Context(), tenantID, email)
		if !hasPasskey {
			// MFA not satisfied — will show passkey registration after consent
		}
		_ = hasPasskey
	}

	masked := security.MaskEmail(email)

	// Check consent
	granted := h.consentGranted(r.Context(), u.UserID, masked)
	if granted {
		if err := h.issueCookies(w, r, u.UserID, tenantID, "auth.otp.verify"); err != nil {
			slog.Error("IssueTokens failed", "err", err)
		} else {
			w.Header().Set("HX-Redirect", "/dashboard")
			w.WriteHeader(http.StatusOK)
		}
		return
	}

	templ.Handler(authtmpl.ConsentPartial(email)).ServeHTTP(w, r)
	h.logSecurity(r, audit.SecurityEvent{
		UserID:      u.UserID,
		TenantID:    tenantID,
		MaskedEmail: masked,
		Action:      "auth.otp.verify",
		Status:      "success",
		ClientIP:    mw.GetClientIP(r),
		ReqID:       mw.GetRequestID(r.Context()),
	})
}

func (h *AuthHandler) ConsentPage(w http.ResponseWriter, r *http.Request) {
	email := r.URL.Query().Get("email")
	if email == "" {
		http.Redirect(w, r, "/", http.StatusSeeOther)
		return
	}
	templ.Handler(authtmpl.ConsentPartial(email)).ServeHTTP(w, r)
}

func (h *AuthHandler) ConsentAccept(w http.ResponseWriter, r *http.Request) {
	email := r.FormValue("email")
	mw.SetLogEmail(r, email)
	gdpr := r.FormValue("gdpr")
	nis2 := r.FormValue("nis2")
	soc2 := r.FormValue("soc2")
	if email == "" || gdpr == "" || nis2 == "" || soc2 == "" {
		templ.Handler(authtmpl.ConsentPage(email)).ServeHTTP(w, r)
		return
	}

	emailHash := security.HashEmail(email, h.pepper)
	u, err := h.users.FindByEmailHash(r.Context(), emailHash)
	if err != nil || u == nil {
		slog.Error("user not found on consent accept", "email", security.MaskEmail(email))
		http.Error(w, "user not found", http.StatusBadRequest)
		return
	}

	tenantID := u.TenantID
	if tenantID == "" {
		tenantID = "public"
	}

	masked := security.MaskEmail(email)
	_ = masked // used for logging

	// Log consent event (v2)
	if h.audit != nil {
		_ = h.audit.LogSecurityEvent(r.Context(), audit.SecurityEvent{
			UserID:      u.UserID,
			TenantID:    tenantID,
			MaskedEmail: masked,
			Action:      "auth.consent.accept",
			Status:      "success",
			ClientIP:    mw.GetClientIP(r),
			ReqID:       mw.GetRequestID(r.Context()),
		})
	}

	// No extra cookies — email is in the template, tenant from email domain

	if h.passkeys != nil && h.passkeys.HasPasskey(r.Context(), tenantID, email) {
		slog.Info("passkey presence: true", "email", masked)
		_ = h.issueCookies(w, r, u.UserID, tenantID, "auth.consent.accept")
		w.Header().Set("HX-Redirect", "/dashboard")
		w.WriteHeader(http.StatusOK)
		return
	}
	slog.Info("passkey presence: false, showing register", "email", masked)
	templ.Handler(authtmpl.RegisterPartial(email)).ServeHTTP(w, r)
}

func (h *AuthHandler) Refresh(w http.ResponseWriter, r *http.Request) {
	cookie, err := r.Cookie("loxtu_refresh")
	if err != nil {
		http.Error(w, "unauthorized", http.StatusUnauthorized)
		return
	}
	pair, err := h.tokens.Rotate(r.Context(), cookie.Value)
	if err != nil {
		http.Error(w, "unauthorized", http.StatusUnauthorized)
		return
	}
	setAuthCookies(w, pair)
	w.WriteHeader(http.StatusNoContent)
}

func (h *AuthHandler) Logout(w http.ResponseWriter, r *http.Request) {
	userIDHash := ""
	userID := ""
	tenantID := "public"
	if c, err := r.Cookie("loxtu_access"); err == nil {
		token, _, _ := jwt.NewParser(jwt.WithoutClaimsValidation()).ParseUnverified(c.Value, &identity.AccessClaims{})
		if token != nil {
			if claims, ok := token.Claims.(*identity.AccessClaims); ok {
				userIDHash = claims.Subject
				if claims.TenantID != "" {
					tenantID = claims.TenantID
				}
			}
		}
	}

	// Resolve raw UUID from user_id_hash (session store uses raw UUID)
	if userIDHash != "" && h.users != nil {
		u, err := h.users.FindByUserIDHash(r.Context(), userIDHash)
		if err == nil && u != nil && u.UserID != "" {
			userID = u.UserID
		}
	}

	slog.Info("logout", "user_id_hash", userIDHash, "reqid", mw.GetRequestID(r.Context()))
	if userID != "" {
		if err := h.tokens.RevokeAllForUser(r.Context(), userID); err != nil {
			slog.Warn("session revocation skipped", "err", err)
		} else {
			h.logSecurity(r, audit.SecurityEvent{
				UserID:   userIDHash,
				TenantID: tenantID,
				Action:   "auth.logout", Status: "success",
				ClientIP: mw.GetClientIP(r), ReqID: mw.GetRequestID(r.Context()),
			})
		}
	}
	clearAuthCookies(w)
	if r.Header.Get("HX-Request") == "true" {
		w.Header().Set("HX-Redirect", "/")
	} else {
		http.Redirect(w, r, "/", http.StatusSeeOther)
	}
}

func (h *AuthHandler) issueCookies(w http.ResponseWriter, r *http.Request, userID, tenantID, action string) error {
	pair, err := h.tokens.IssueSession(r.Context(), userID, tenantID, "worker", nil)
	if err != nil {
		return err
	}
	h.logSecurity(r, audit.SecurityEvent{
		UserID:   userID,
		TenantID: tenantID,
		Action:   action, Status: "success",
		ClientIP: mw.GetClientIP(r), ReqID: mw.GetRequestID(r.Context()),
	})
	setAuthCookies(w, pair)
	return nil
}

func (h *AuthHandler) consentGranted(_ context.Context, _, _ string) bool {
	// TODO: implement consent check against user_consents table
	return false
}

func (h *AuthHandler) logSecurity(r *http.Request, ev audit.SecurityEvent) {
	if h.audit == nil {
		return
	}
	_ = h.audit.LogSecurityEvent(r.Context(), ev)
}

func setAuthCookies(w http.ResponseWriter, pair identity.TokenPair) {
	http.SetCookie(w, &http.Cookie{
		Name: "loxtu_access", Value: pair.AccessToken,
		Path: "/", MaxAge: 900,
		HttpOnly: true, Secure: true, SameSite: http.SameSiteLaxMode,
	})
	http.SetCookie(w, &http.Cookie{
		Name: "loxtu_refresh", Value: pair.RefreshPlain,
		Path: "/", MaxAge: 86400 * 30,
		HttpOnly: true, Secure: true, SameSite: http.SameSiteLaxMode,
	})
}

func clearAuthCookies(w http.ResponseWriter) {
	http.SetCookie(w, &http.Cookie{Name: "loxtu_access", Value: "", Path: "/", MaxAge: -1, HttpOnly: true, Secure: true})
	http.SetCookie(w, &http.Cookie{Name: "loxtu_refresh", Value: "", Path: "/", MaxAge: -1, HttpOnly: true, Secure: true})
	clearTempAuthCookies(w)
}

func clearTempAuthCookies(w http.ResponseWriter) {
	// No temp auth cookies are set in the current flow.
	// This function exists as a hook for future use.
}

// emailDomain extracts the domain part of an email address.
func emailDomain(email string) string {
	parts := strings.SplitN(strings.TrimSpace(email), "@", 2)
	if len(parts) != 2 || parts[1] == "" {
		return ""
	}
	return strings.ToLower(parts[1])
}
